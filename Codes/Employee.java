public class Employee{
	private String name;
	private int age;
	private float salary;
	
	public void setName(String tName){
		name = tName;
	}
	public String getName(){
		return name;
	}
	public void setAge(int tAge){
		age = tAge;
	}
	public int getAge(){
		return age;
	}
	public void setSalary(float tsalary){
		salary = tsalary;
	}
	public float getSalary(){
		return salary;
	}
}